package d0427.p_68;

public class OperatorExample {

	public static void main(String[] args) {
		boolean bPlay = true; //변수명 짓는 규칙 -> _변수타입을 앞에 씀
		
		System.out.println(bPlay);
		
		bPlay = !bPlay;
		System.out.println(bPlay);
		
		bPlay = !bPlay;
		System.out.println(bPlay);

	}

}
